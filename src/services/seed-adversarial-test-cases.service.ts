/**
 * Security Test Suite Catalog + Minimal Persona Auto-Seeds
 *
 * - `getSecuritySuiteCatalog()` returns the FULL 75-test security suite sourced
 *   from `security.xlsx` (Security, PII, Toxicity, Robustness, Behavioral,
 *   Compliance). These are NOT auto-applied. They are exposed through the
 *   security-suite picker UI so users select which tests they want for a given
 *   agent, then attach them on demand.
 *
 * - `getSeedAdversarialTestCases()` returns ONLY a minimal set of persona /
 *   behavioural seeds (rude customer + interruption handling) that should still
 *   be auto-added to every new agent's test set so the generator never ships an
 *   agent without basic robustness coverage.
 *
 * Each entry includes:
 *  - name             : human-readable label prefixed by Test ID (e.g. "SEC-PI-01: ...")
 *  - scenario         : adversarial input + numbered attack prompts the test-caller LLM should send
 *  - expectedOutcome  : pass criteria from the spec (refusal = PASS)
 *  - category         : top-level grouping (Security / PII / Toxicity / Robustness / Behavioral / Compliance)
 *  - priority         : mapped from spec (CRITICAL/HIGH=high, MEDIUM=medium, LOW=low)
 *  - is_security_test : true for spec entries
 *  - security_test_type: granular type used by persona-prompt-builder for objective + rubric
 *  - test_id          : spec ID (e.g. SEC-PI-01), preserved for analytics + KPI rollups
 */

export interface SeedTestCase {
  name: string;
  scenario: string;
  expectedOutcome: string;
  category: string;
  keyTopic: string;
  priority: 'high' | 'medium' | 'low';
  // Persona / security fields persisted to test_cases table
  persona_type?: string;
  behavior_modifiers?: string[];
  is_security_test?: boolean;
  security_test_type?: string;
  /** Original spec ID (security.xlsx) - preserved for KPI grouping in dashboards. */
  test_id?: string;
}

